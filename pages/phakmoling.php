			<p>Skyagum village is about 15 minutes by jeep or 30 minutes by foot from Manda. The road climbed steadily and the valley narrowed, until we were
			almost at the snow line. We walked up to the monastery above the village along narrow paths winding between fields and small, fast-flowing streams.
			The landscape was almost treeless, but the nuns recently planted some trees in front of the gonpa. We were welcomed by the nuns, a drummer, and a
			magnificent sunset. From the guest room, with its two walls of windows, we looked out on shafts of brilliant pink and gold rays of sunlight touching
			the jagged snow-covered mountains. Fire and snow in balance.</p>

			<p>A lama from Karsha Monastery was posted in Skyagum Village to assist the villagers and nuns in their practice until 2008. He was sad to be sent to
			another village because the nuns were so eager to study the texts. The lama was very supportive of the nuns and encouraged and assisted them in many
			ways, not only with learning texts, but also sewing banners for the windows, etc.</p>

			<p>The 18 nuns living in the monastery are between 20 and 36 years old. All became nuns in their teens, having made this decision themselves,
			although some also acknowledged receiving advice from their parents. Four young girls in the village around 12 years of age plan to become nuns when
			Rizong Rinpoche visits Zangskar.</p>

			<p>For the last 15 years, there has been a school in Skyagum that is attended by both boys and girls. Two of the nuns had attended school up to
			second grade, but said they had forgotten most of what they learned.</p>

			<p>The monastery has an assembly hall with a glassed-in veranda and an adjoining kitchen. This will become a guest room once the new kitchen at the
			back of the gonpa is completed. The nuns have individual accommodations and most have two rooms including a toilet; one nun (who has no parents)
			has only one room. The nuns have built a toilet and a washroom. There is a shrine room that villagers visit on special puja days and other auspicious
			occasions to make offerings.</p>

			<p>The nuns arise at 5am in summer and at 6am in winter. They perform pujas for about two and a half hours, including offerings of water bowls and
			butter lamps, chanting, and prayers. Breakfast is prepared and taken individually around 8am. After breakfast in summer, all the nuns work in the
			fields, partly because their families cannot support them without this assistance. In autumn, the nuns collect wood and cow dung for fuel. Winter is
			the time for more intense practice. After the evening meal, the nuns study Dharma texts, sometimes until midnight. The nuns appreciate living in the
			monastery, as this provides them with the opportunity for practice in the mornings and evenings. They feel more relaxed here, since at home their
			families are always busy. The nuns meet as a group with the lama to discuss such issues as building plans, planning pujas, and planting trees.</p>

			<p>The nuns study the teachings one to two hours each evening and during most winter days. They are learning to recite Lama Ch√∂pa, Monlam Namgye,
			and Lamgyi Namsum, and will be able to do the practices once they have learned the texts. They have received no other teachings. Puja days include
			the fifteen days of Monlam during the fourth month (in summer) and a range of auspicious days when villagers offer food and make tsog offerings.
			On full moon days, there are fasting rituals and, during the first lunar month, the nuns perform a puja and fasting ritual in another village. The
			day we left was the day the villagers circumambulate the gonpa carrying the Dharma texts.</p>

			<p>Contact with the local community is especially close when deaths in the family occur and on certain puja days. Villagers tend not to come at the
			time of births, but when deaths occur, they ask the nuns to visit their homes to perform pujas and prayers. Before, they often asked the lama to
			visit their homes, but he suggested that they come to the shrine room instead. Some villagers come three times a year to do pujas, but have not
			assisted with the building project. The nuns themselves assist the builder and carpenter and cook for them. The nuns do not give teachings to people
			in the village, as they themselves have had limited teachings. Three of the nuns went to Karsha Monastery for a short Vinaya training, but did not
			understand much of the teachings.</p>

			<p>The nuns were very happy to have a teacher this past year, which especially benefited the younger nuns. The nuns would like to build more rooms
			and be able to invite more teachers. They would also like to have thangkas for the shrine room. The nuns are able to read Tibetan well and are
			learning composition as well. They would like to become a branch of CIBS (Central Institute of Buddhist Studies).</p>
