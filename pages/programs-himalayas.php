			<p>Traditional values are still strong in this area, which has been home to a unique Buddhist culture for over 1000 years. Loving kindness and care for the young,
			old, and infirm has been the core of women's spiritual practice over the centuries. Rarely, however, have women emerged as spiritual leaders, due to a lack of
			educational opportunities.</p>

			<p>With the Tibetan border now sealed and access to their cultural heartland denied, the threat of cultural extinction in the Himalayan region looms large. Social
			and economic changes seriously endanger traditional values. It is now more urgent than ever that women be granted equal social and educational opportunities.</p>

			<p>The work of establishing schools has not been easy for the women of the Himalayas. Plagued by poverty, they face many social and personal obstacles. Gradually,
			however, through sheer determination, these obstacles are being overcome.</p>

			<p>There are now two Jamyang Foundation projects in Spiti, Yangchen Choling Monastery and Dechen Choling Monastery. An additional project, Sherab Choling Institute
			in Morang, is now self-sufficient.</p>

