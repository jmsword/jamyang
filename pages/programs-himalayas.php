		<h4>Zangskar Valley</h4>
		<div class="large-12 columns">
			<p>Since 1988, Jamyang Foundation has provided support to nuns’ monasteries in Zangla, Pishu, and Karsha for food, books, teachers, robes, and various construction
        projects. Gradually, we expanded our support to all the nuns’ monasteries in the region, providing women with the opportunity to live a monastic life with time for
        the study and practice of the Dharma – an opportunity they greatly appreciate.</p>

			<p>Jamyang Foundation currently supports nine monasteries in Zangskar. Prior to 1988, very few of the nuns had the chance to attend school. In the last 20 years, however,
      more schools have opened and these days most parents want their children to get an education. Women who are keenly interested in Dharma, however, prefer to receive a
      traditional monastic education. Now all the nuns are competent in Tibetan and enjoy reading Buddhist texts.</p>

			<p>The Ladakh Nuns Association (LNA) visited eight monasteries for nuns in Zangskar in 2002. Much of the information here is taken from their survey. Although there have
      been many changes since then, mostly improvements, this information about the lives of nuns in Zangskar at that time are very valuable. Read about the
      <a href="nunsofzangskar.php">Nuns of Zangskar</a> and then click the links below to learn more about each of the monasteries and the intrepid nuns who built and
