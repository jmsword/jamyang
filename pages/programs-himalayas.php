			<p>Two Jamyang Foundation schools are located in the snowy land of Spiti, a desolate region surrounded by high Himalayan peaks. <a href="yangchencholing.php">Yangchen Choling</a> is located in the
			village of Pangmo. <a href="dechencholing.php">Dechen Choling</a> is located in Pin Valley. Life is arduous in Spiti, especially during the frozen winter months.</p>

			<p>Due to heavy snowfalls, transport and communications to the Himalayan region are impossible much of the year. Economic hardship, particularly disruption of the
			wool trade from occupied Tibet, has led the men of Himalayan areas to seek jobs in Indian cities. Women now shoulder a heavy responsibility for both family and
			livelihood</p>

			<p>Intense cold and high winds prevail outdoors, so handicrafts and Buddhist practice – meditation, teachings, and prayer – are favorite winter activities. The
			solace of these spiritually-enriching activities helps offset the sufferings of the harsh environment.</p>

			<p>Traditionally women's work has centered around the home – spinning, knitting handcrafted clothing, and caring for the family, animals and fields. Women's
			education and spirituality have also centered around the home. Buddhist culture has flourished here since the 8th century, but there has never been a traditional
			learning center for women.</p>
