		<h4>Surveying the Monasteries</h4>
		<div class="large-12 columns">
			<p>The Ladakh Nuns Association (LNA) visited eight monasteries for nuns in Zangskar in 2002. Much of the information here is taken from their survey.
			Overall, there have been significant improvements for nuns in Zangskar in recent years, but their needs are still great. The nuns have have a strong
			yearning to deepen their study of the Dharma. Prior to 1988, most of the nuns of Zangskar worked in their families’ homes or in their fields, with
			practice basically restricted to the winter retreat. Since 1988, the monasteries (Zangla, Pishu and Karsha) have received financial support from Jamyang
			Foundation for basic food, robes, Dharma texts, and the construction of rooms. This support has given some nuns the opportunity to live in a monastery
			and have more time for the study and practice of the Dharma, which they greatly appreciate.</p>

			<p>Jamyang Foundation currently supports nine monasteries. Prior to 1988, virtually none of the nuns had been to school. Over the last 15 years, however,
			schools have been opened in Zangskar and these days most parents want an education for their children. One problem is that the medium of instruction in
			most schools is Urdu, so children are forgetting their own language. Now all the nuns have learned Tibetan script and can read the Dharma texts. Some of
			the monasteries have periodically had a resident lama and regular teachings, but most of the nuns had received only occasional teachings. Changchub Choling
			Monastery in Zangla and Khacho Drubling Monastery in Karsha have study programs provided through CIBS (Central Institute of Buddhist Studies) with classes
			in Hindi, English, Ladakhi, math and Buddhist philosophy. Other monasteries have made strong requests for similar study programs.</p>

			<p>Many of the nuns are requested to work in their family fields over the summer months, which takes time away from their Dharma study and practice. Due to
			limited access to Dharma teachings, the nuns generally lacked confidence and the knowledge they need to be able to give even basic teachings to their families
			and communities. Some of the monasteries had active community assistance in repair work, etc., and all were involved with their nearby villages. The local
			villagers came to offer and sponsor pujas on auspicious days or asked the nuns to read texts for them.</p>

			<p>The vision and hopes for the future of the nuns of Zangskar was seen mainly in terms of a longing for teachings and the need to attract younger nuns.
			These two needs are connected. If there were more young nuns, they felt there was a greater chance of attracting a teacher; having a teacher would enrich the
			monastery and attract more young women to become nuns. However, funds are needed for this vision to be achieved, especially funds to build facilities, such
			as classrooms and rooms for teachers, and to purchase thankas, Dharma texts, and further construction projects.</p>

			<p>Over the last 20 years, some young nuns from Zangskar have been studying in Dharamsala, Mundgod, and Bhutan. The monasteries hope that these nuns will return
			to Zangskar to teach younger nuns when they complete their studies. They also hope that these nuns will return to Zangskar during the summer months to give
			teachings and encourage the younger nuns.</p>
