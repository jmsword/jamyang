		<h3>Nuns of Zangskar (Ladaka)</h3>
		<div class="large-12 columns">
			<p>Zangskar is in a mountain range south of Ladakh. It is still an isolated place, cut off by snow for 7 to 8 months of the year,
			but the ancient cycle of rituals to mark the seasons, moon cycles, and events in the religious calendar continues. Winters are
			harsh, with temperatures falling below 30 degrees C, and the snow is often deep, and has to be daily swept off the roof tops. There
			are still few cars or motorised transport and the only phones are found at military or security posts.</p>

			<p>Many of the traditional livelihood practices and basic self-reliance continue. Crops are cultivated and animals taken to higher
			slopes in summer. The growing season is limited to 3 months of the year, with barley and peas as the main crops. Animals such as yak,
			sheep and goats are a means of transport, and provide milk, meat and clothing. In most villages, women spin the wool, men weave and the
			cloth is sewn into clothes. Winter is a time for retreats, teachings, and festivals for both monastics and laypeople.</p>

			<p>Over the last 2 years, the snowfall has been greatly reduced. In some of the monasteries, water supplies are very limited, with only
			a trickle flowing through the pipe. The nuns have planted trees around their monasteries, but some have had to be abandoned due to recent
			water shortages. Other changes result from outside influences. The closing of the border with Tibet and the poverty of the region have all
			made it difficult to maintain the unique spiritual and cultural heritage of Zangskar.</p>

			<p>The monks’ monasteries are in a better position than nuns’ monasteries, because the villagers are more likely to support monks. Historically,
			monasteries have been well endowed with land holdings and regular donors. The situation in most of the monasteries is one of great need. Prior to
			1988, the nuns of Zangskar were mostly working in their families’ homes, where they cooked, cleaned and looked after the children and animals.
			They practice meditation mostly in the winter months when their families provided tsampa for retreats.</p>
		</div>
