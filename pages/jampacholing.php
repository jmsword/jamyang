			<p>In 2000, Tenzin Uphal, a widow from Meeru decided to donate a piece of land for the building of a Buddhist monastery for women. At that time,
        there was no Buddhist nunnery in Kinnaur. Young women who aspired to become Buddhist nuns had to leave the area and go to Dharamsala to study,
        if they were lucky enough to be admitted. The founding of Jampa Choling Monastery attracted 25 young women from the area who started building
        the nunnery with their own hands. The project was under the guidance of Tenzin Dolma, a devout, older Buddhist woman from Meeru. Tenzin Uphal,
        the donor of the land, became a nun at Jampa Chöling and lived there until her death in 2014. Thanks to her kindness and the generosity of many
        donors, the monastery has continued to grow.</p>

			<p>For the first years, before they were able to build Jampa Chöling, the young women lived as renunciants in a small room in the village temple in
        Meeru. During the process of building the monastery, some women left to go to study in Ladakh and Dharamsala, discouraged by the hard work, lack of
        facilities, and lack of studies. In summer 2003, the remaining 17 women started living in the nearly finished monastery and received the precepts of
        a nun. That summer, two young nuns from Dharamsala came for three months to get the new nuns started with their studies and prayers. Due to the
        remoteness of the area and the lack of facilities, it took a huge effort to find a teacher willing to stay at the monastery on a permanent basis.
        In the summer of 2004, Jamyang Foundation was finally able to find a fully qualified teacher for the nuns. Gen Jangchup Yarphel taught at the monastery
        for six years and, when he was called away, the nuns were able to find two other fine teachers.</p>
