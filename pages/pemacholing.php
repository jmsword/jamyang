			<p>There are eight young nuns between the ages of 16 and 31 living in the monastery. All became nuns in their teens, this being their own decision. Three young girls
			between the ages of 8 and 12 who planned to become nuns were living at home with their families, but came daily to the monastery to study Dharma texts. None of the nuns
			had been to school; although there was now a school in Manda, the novices only attended occasionally.</p>

			<p>The monastery was only six years old, with a shrine room and three rooms that the nuns shared. The gonpa is being constructed mostly by the nuns themselves; they work
			as manual laborers during the summer, so they can pay a builder to help them. There has not been much support from the village.</p>

			<p>The nuns rise at 5am, offer prayers to Avalokiteshvara, and perform the Lama Chodpa puja. Breakfast is mostly prepared and eaten individually, but occasionally
			together. From 10.30am until midday, the nuns study Dharma texts and practice Ngondro and Lama Chodpa. After lunch in winter, they may read texts for the villagers. In
			the autumn, they collect wood from the mountains. In summer, they help their families and do paid labor. After their evening meal, the nuns chant mantras until about
			11pm. The nuns said that the benefit of living in the monastery is that they are now able to have more time to study Dharma texts.</p>

			<p>There is no resident teacher at Manda Monastery and the nuns have received only occasional teachings. They received teachings from His Holiness the Dalai Lama near
			Sani for 5 days around 1998. They recently attended a one-day teaching with a lama from Dharamsala at the nearby Skyagum monastery. The nuns have received no other
			training.</p>

			<p>The nuns regularly meet together to discuss the plans for their monastery and make decisions collectively. Contact with people from the village is occasional, such
			as at Monlam, held from the first to the fifteenth day of the fourth lunar month, when the villagers come to offer food and tea. The villagers helped when stones were
			being collected for the construction of the gonpa.</p>

			</p>The nuns hope to build a large monastery that will benefit the village and all living beings. When the main buildings have been completed, they would like to
			construct stupas and acquire thangkas and Dharma texts. They also long to receive teachings, but because they do not have a teacher, could not be more specific about
			which teachings. However, they said they would also appreciate the opportunity to study of Hindi, English, etc., provided by CIBS (Central Institute of Buddhist Studies).</p>
