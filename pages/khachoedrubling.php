	<div class="row">
  <hr/>
		<div class="large-12 columns">
			<p>About 15 minutes drive from Padum, over a motorable bridge, lies Karsha Monastery for monks and Khachoe Drubling Monastery for nuns, separated
			by a gully. From the village below, it is a short walk straight up the mountain. The walk begins on rocks in a wide, shallow streambed, then
			continues up a steep narrow ascent. There are 17 nuns and a resident teacher at the monastery.</p>

			<p>All the nuns at Khachoe Drubling have been nuns since their teens. Most say it was their own decision to become a nun. The early nuns walked to
			many villages to request support for the monastery. Of the 22 nuns living at the monastery, half are under 30 and 6 are over 50. The youngest nun
			is 6 years and the oldest is 77. Another remarkable nun of 76 years was one of the original 5 nuns who founded the monastery and the only one still
			alive. She became a nun when she was 10 and started to build the monastery 10 years later.</p>
    </div>
	</div>

  <div class="row">
		<div class="large-7 columns">
			<p>The old temple, which was originally used as the gonpa, is said to be at least 1,000 years old. It is small and cave-like, with old paintings on
			the walls and many butter lamps on the altar. There is a very tall statue of Avalokiteshvara behind the main altar. Laywomen were performing the
			fasting retreat (nyungne) in this temple when we visited. At lunchtime, they rested on the rocks around the shrine room or circumambulated the
			temple.</p>

			<p>The buildings at Karsha monastery include an assembly hall for prayers, shrine room, kitchen, and accommodations. Summer accommodations are
			located above and winter rooms below, shared by two nuns. Most accommodations had two rooms, one for sleeping and cooking and another used as a
			shrine room. There was no classroom, and classes were held in the teacher’s room in summer until 2006 when a new classroom was built. The nuns plan
			to build more accommodations for the younger nuns.</p>

			<p>Gelongma Karma Lekshe Tsomo has provided funds for the monastery since 1991, with support for food, teacher’s salary, robes, and texts. A nun
			named Thukche Dolma was trained in basic health care in Leh for a year. Only one nun studied up to 4th grade and the rest have not been to school.
			The nuns learned Ladakhi from their parents or others. The younger nuns now study in classes provided through CIBS (Central Institute of Buddhist
			Studies) since 2007.</p>
    </div>
    <div class="large-5 columns">
        <a href="../img/khachoe/Khachoe_1212.jpg"><img src="../img/khachoe/Khachoe_1212.jpg"/></a>
    </div>
  </div>

  <div class="row">
    <div class="large-12 columns">
			<br/><p>The nuns get up each day at 6am in winter and 5am in summer. They do puja in their rooms for an hour or more, followed by breakfast and washing.
			When their teacher Geshe Rinchen Gyaltsen was still alive, they studied Buddhist philosophy for two hours in the morning and again from 3-5pm. Lunch
			is taken together in the summer months and individually in the winter. In summer, all the nuns gather for puja from 5-10pm, but it is too cold to do
			so in the winter. In summer, the nuns get regular exercise by walking in the mountains to collect cow dung. In winter, they have to regularly have
			to shovel from the roof tops and carry water up the hill.</p>

			<p>For the nuns, the benefit of living in the monastery is not having to work on their families’ land, which allows more time for practice. The nuns
			still assist their families a little when needed, but they feel that it is more important to do pujas to benefit their families and all living beings.</p>
    </div>
	</div>

  <div class="row">
    <div class="large-5 columns">
        <a href="../img/khachoe/Khachoe_1898.jpg"><img src="../img/khachoe/Khachoe_1898.jpg"/></a>
    </div>
    <div class="large-7 columns">
			<p>The nuns received teachings from Rinchen Gyalson from 1996 until his death in 2004. Before that, they received many years of teachings from a
			senior scholar of Karsha Monastery named Kachen Ngawang Tharpa. The nuns explained that they studied logic in the mornings and debate in the
			afternoons. When asked whether the study of debate had increased their knowledge and confidence, they agreed that it did. One nun who is 42 feels
			that she is too old to learn much.<p>

      <p>Decisions regarding the monastery are made when all the nuns and the teacher meet together. Families visit the monastery on auspicious occasions
			like the 15th day of the lunar month (full moon) and may ask the nuns to do pujas for them.</p>
    </div>
  </div>

  <div class="row">
    <div class="large-12 columns">
			<p>Nuns sometimes visit their families on Sundays. Most nuns are from Karsha Village, but some are from other nearby villages. If people ask the nuns
      for teachings, they are ready to give them, but usually they are not asked. Geshe-la gave the nuns basic teachings on refuge and the Four Noble Truths
      and encouraged them to teach. He also taught them Tibetan language using textbooks published in Dharamsala. The nuns attended a training program in
      Western healthcare.</p>

			<p>The nuns’ main hope for the future is to further their education. They feel that if they could provide better facilities for teachers and had
			more support for food, they would have greater access to education. They are very happy to have a teacher from CIBS (Central Institute of Buddhist
			Studies), so that they can extend the range and depth of their studies. They would also like to have a resident lama to teach in-depth Buddhist
			studies.</p>
    </div>
