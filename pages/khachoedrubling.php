  <h3>Khachoe Drubling Monastery (Karsha)</h3>
      <div class="large-12 columns">
          <a href="../img/khachoe/Khachoe_0975.jpg"><img src="../img/khachoe/Khachoe_0975.jpg"/></a>
      </div>
