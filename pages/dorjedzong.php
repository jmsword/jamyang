	<h3>Dorje Dzong Monastery (Dorge Dzong)</h3>
		<div class="large-12 columns">
			<p>This monastery was on the same side of the Zangskar River as Karsha, a three-quarters of an hour drive away. We climbed up and up a steep winding
			road, with the nearest village 30 minutes walk away. The monastery buildings seemed to emerge from the stony ground, with little in the way of trees
			or grasses. Water this year was particularly scarce – a dribble from a pipe five minutes’ walk from the main buildings. Even in summer, the harsh
			wind and biting cold made one find the shortest means possible for washing.</p>

			<p>There are 11 nuns living at Dorjezong monastery, 10 under 30 and 2 over 50. The youngest nun is 16 and the oldest is 73. All had been nuns since
			their teens and all said their parents had advised them to become nuns. None of the nuns had attended school, but all had learned Ladakhi from their
			parents or relatives.</p>

			<p>The monastery has a shrine room and accommodation for all the nuns, but no classroom or library. Some nuns share accommodations, but most have
			individual rooms with a room downstairs for winter and one upstairs for summer. The rooms are small and simple, built from stones collected nearby,
			with the support from the nuns’ families. The doors leading into the rooms are narrow and low, to minimize heat loss during the winter. Each room
			has a traditional compost toilet constructed inside. There were no plans for further building.</p>

			<p>The nuns arise at 5am in winter, but a little later in summer because they have to work in the fields. Morning prayers last for about an hour,
			followed by breakfast, which the nuns prepare and eat on their own. During the 4 months of summer, the nuns usually work 2-3 days a week for 8
			hours a day helping their families with agricultural work. On other days, they collect wood and dung from higher up the mountains. On work days,
			the nuns return to the monastery around 6-7pm; if they work later, they may stay with their families. Prayers are offered in the evening for around
			one and a half hours in their own rooms.</p>

			<p>Because there is currently no teacher, the nuns are not studying the Dharma texts, but all are most eager to study, especially the younger nuns.
			If a resident teacher is not available just now, they would be happy to have a student teacher, such as a nun currently studying in Dharamsala, for
			any length of time. They have not approached Karsha Monastery (which could mainly offer teachings on ritual, etc.), as they want to study Buddhist
			philosophy, English, Hindi, etc. They have sent an application to the CIBS, but have not received a reply.</p>

			<p>The nuns saw living in the monastery both as a benefit for others and themselves, because it allows more time for practice. The oldest nun, who
			has lived in the monastery over 55 years, said the daily practice has not changed much over the years. The practice includes the fasting ritual and
			Monlam in winter for a month. They recite memorized texts and a long life prayer for His Holiness the Dalai Lama. Repairs to the assembly hall have
			been made and the new younger nuns have built their own rooms. The only teachings they receive are once a year when they invite a lama from Karsha
			Monastery to give teachings and initiation for a long life puja.</p>

			<p>The nuns perform fasting pujas on full moon day, sometimes sponsored by some people from the village. Occasionally, together with monks from Karsha,
			the nuns perform long-life ceremonies that was attended by people from the village. The nuns meet together as a committee to discuss issues that affect
			them all, such as the need for repairs to the assembly hall. People from the village visit 3-4 times a year, for Monlam and fasting pujas. Because
			they have not received teachings, they lack the confidence to give teachings to their families and the community. The only training any of the nuns
			have attended is the one week of Vinaya training, which three nuns attended.</p>

			<p>The nuns hope that more young girls will enter the monastery and would like to have a good study program to offer them. A resident teacher would
			attract young girls to become nuns. Five nuns from the monastery left to study in Dharamsala 15 years ago and the resident nuns very much hope that
			some of these nuns will return to teach. We learned that Ngari Rinpoche, a brother of H.H. the Dalai Lama in Dharamsala, had visited their monastery
			once. Getsulma Tenzin Lhadron, a student at Jamyang Choling in Dharamsala, will talk with the Dorje Dzong nuns now in Dharamsala and Ngari Rinpoche
			to explore future possibilities.</p>

			<p>One nun named Lobsang Wangmo is now 27 and became a nun when she was 15. Her parents and three siblings are from a nearby village and she helps
			them cultivate barley, wheat, and peas. The family has 5 dzomos and 3-4 goats. Water has become a major problem in recent years with inadequate
			snowfalls. In summer, Lobzang Wangmo works with her family and, in winter, she reads Dharma texts. She says she has little understanding of the
			Dharma, but welcomes guidance, as this would be a benefit in this life and the next.</p>
