		<h3>Samten Choling Monastery (Tungri)</h3>
		<div class="large-12 columns">
			<p>It takes three quarters of an hour to drive from Dorjezong monastery to Tungri monastery, which is about a half-hour drive from Padum. There is a
			road into Tungri village off the main road, just by the bridge over the river from on the road from Padum. Tungri Village has 36 families with a total
			of 400 people. Nearby are Shunling and Trahen Villages. A large group of women from the Women’s Alliance are active in Tungri Village.</p>

			<p>The monastery is close to the high snow-covered mountains and a large village nestled by the mouth of a stream, with much greenery around. Forestation
			programs are being undertaken by the Forest Department of Kargil and the Desert Development Agency. A government department installed a water pipe across
			to the monastery, but the water had recently dried up, so the nuns cannot grow vegetables and their trees are dying.</p>

			<p>The monastery was founded in the same period as the nearby Sani Kanihar Palace. The paths around the monastery are well-swept and the rooms ar clean.
			The room where guests are welcomed has a clay floor with mats placed for sitting. There are simple low tables on which tea is served in the usual round
			of hot water, sweet milk tea, and salty butter tea and tsampa.</p>

			<p>Of the 11 nuns living in the monastery, two are under 30 years and six are over 50; the youngest is 26 and the eldest is 80. None of the nuns has been
			to school, mostly because schools have only been established quite recently. There is now a school in Tungri up to the 8th grade and the nuns said that most
			children in their families go to school. Education is regarded as important and these days as many girls as boys go to school.</p>

			<p>One nun, Getsulma Tenzin Wangmo, is now in her second year of a five-year training to be an amchi. This training is held in Tungri Village, with 20 students
			selected from 20 surrounding villages. The school was established by the teacher, Tseten Dorje, who has connections with France. Tenzin Wangmo’s father is also
			an amchi, but this year she has been unable to attend many classes, because she has been given the duty of shrine keeper.</p>

			<p>The monastery has a gonpa in the same building as the kitchen and guest room. The latter is used for prayers in the winter, because it is warmer. There is no
			library or classroom. The nuns have a set of the Kangyur (108) texts, but have no space to put it at present. Rooms for the nuns (downstairs for winter and upstairs
			for summer) are mostly individually occupied, though some are shared. There are no plans for further building. The nuns meet together as a group to discuss specific
			issues or decisions to be made, such as the selection of a storekeeper, painting of the stupa, or repair of the assembly hall.</p>

			<p>The nuns wake up at 5am. A couple of hours are spent cleaning their rooms and offering water bowls and prayers, followed by breakfast. In summer, the younger nuns
			collect wood and cow dung for winter supplies of fuel and may help their families for about 2-3 days per month. The older nuns said they worked a lot when they were
			younger. Their families provide limited but adequate food support for the nuns; the grandchildren help carry water and collect wood for them. When there are pujas at
			the monastery, the nuns eat together; otherwise they cook for themselves. When they return from work in the afternoon, after tea, they offer prayers for 2-3 hours.
			These include taking refuge, chanting the Guru Puja, and reciting the Avalokiteshvara mantra: Om Mani Padme Hum</p>

			<p>The nuns say there are many benefits of living in the monastery. They have leisure for Dharma practices and prayers, no physical hardships, and are able to benefit
			the villagers through their practice. They appreciate their freedom in the monastery.</p>

			<p>The nuns were fortunate to have a lama from Karsha, Tsering Wangyal, living in the monastery at one time. Twice a month, he gave teachings on the Mahayana precepts,
			explaining the precepts and discussing their benefits. He also gave teachings on ritual during the summer and winter for about seven years. He studied ritual at Karsha
			Monastery and was a retired chanting master. He was appointed to Tungri Monastery to give teachings to the villagers and assist the nuns there. The nuns have not
			received other teachings, although some of the older nuns have been blessed to visit Dharamsala to see H.H. the Dalai Lama. They have seen him also in Leh and Zangskar.</p>

			<p>The nuns perform pujas on the 10th and 25th of each month and during Monlam for a month in the winter. The nuns chant the Vajayogini and Guru Pujas on certain days
			of the month, perform the fasting ritual in the sixth month, and celebrate the Buddha’s first teaching for the villagers. Many people from nearby villages visit the
			monastery on the 10th and 25th days of the month to do prostrations and offer butter lamps. The nuns don’t feel confident enough to give teachings, but they encourage
			the women and children to recite mantras.</p>

			<p>The nuns are concerned about the future of the monastery, as there have been very few new younger nuns entering. They think that if they had a teacher who could teach
			Buddhist philosophy, Tibetan, English, and so on, more girls and young women would be attracted to the life of a nun. Short teachings are sometimes organized at other
			monasteries in Zangskar, but the monastery lacks adequate accommodations and facilities to hold such a program.</p>
