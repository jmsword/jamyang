		<h3>Namgyal Choling Monastery (Pichu)</h3>
		<div class="large-12 columns">
			<p>It is a short drive to the footbridge over the Zangskar River and almost an hour’s walk across a gentle, stony slope to reach Namgyal Choling
			Monastery. Pishu Village, consisting of 25 families with about 200 people, is situated nearby, slightly lower than the monastery. The village youth
			have built a stupa in the village and the Monlam (prayer festival) is held annually. The musical skills of the nuns in their use of the trumpets,
			horns, drums, and gestures (mudras) are very impressive. The nuns stay in retreat during the first month of each year.</p>

			<p>The monastery has received annual support from Jamyang Foundation since 1993. This support has been used to purchase many volumes of the classical
			texts, robes for the nuns, food, and to build a guesthouse with a classroom.</p>

			<p>There are eleven nuns in residence, two of whom are under 30 and five over 50. The oldest is 84 years and the youngest is 25. All became nuns in
			their teens, advised by their parents and older nuns that they would be happy with this decision. All the nuns live at the monastery and each has her
			own individual room. Before becoming nuns, they were all very poor, because the nearby village is poor and does not have a good water supply. None of
			the nuns has been to school, but all learned Ladakhi from their fathers or from others who could read. None of the nuns in residence has undertaken
			further studies, though several young nuns from Pishu are studying in Dharamsala.</p>

			<p>The monastery buildings consist of the gonpa, guesthouse with classroom, and individual accommodations for all nuns. Each nun has a room, which
			includes a small room for winter below one that is used in the summer. Although there is no library, the monastery has many classical Buddhist texts.
			The nuns would like to build a library on top of the new classroom.</p>

			<p>The nuns’ day starts around 6am. After washing and cleaning, they perform morning prayers individually for one and a half to two hours. These
			prayers are shortened when they are invited to read texts in the village. Breakfast is cooked individually when there are no communal pujas or
			prayers. In summer, the younger nuns engage in a range of activities, including trips to the mountains to collect cow dung and wood, reading texts,
			and sometimes, when asked by their families to help, work in the fields. Because they depend on their families for food and support, they feel
			obliged to help out. Both lunch and evening soup are cooked individually.</p>

			<p>If the nuns are present at the monastery in the afternoon, they recite prayers. In the evening, they recite Tara Puja and other prayers.
			Special pujas are held throughout the year, including bi-monthly pujas on the 10th and 25th days of the lunar calendar, a winter puja held during
			the first lunar month, a puja during the fourth month, 17 days for Monlam, and a 3-day fasting puja during the fifth month.<p>

			<p>The nuns agree that staying in the monastery makes practice easier. If they stay in the village, there is more work and it is more difficult to
			practice. The nuns occasionally receive teachings on Lam Rim and Guru Puja from lamas from Karsha Monastery, when the monks have time. Obstacles are
			regarded as being both inner and outer (such as hearing problems) and are regarded as greater for younger nuns, who are more likely to disrobe than
			older nuns.</p>

			<p>Two nuns are selected every 3 years to take responsibility for organising the pujas. All of the nuns gather together periodically to discuss the
			accounts, which are managed by an accountant from the village. There is close cooperation between the monastery and the village. The Women’s Alliance
			assists the monastery, after talking together to see what work needs to be done. Plans include repairs to the stupas and reading texts on auspicious
			days. The Women’s Alliance prepares food on those occasions.</p>

			<p>When buildings need repairs, families from the village often bring wood and other materials and help with the work. The nuns visit people’s homes
			in the village and sometimes villagers visit the monastery. Twice a year, the nuns take the monastery’s sacred texts to the village to read, which is
			seen as bestowing blessings. The older nuns give teachings and Dharma-related advice. All the nuns go on an almsround of the village at harvest time
			and visit nomadic areas to collect butter. When someone dies in the village, the family requests the nuns to perform pujas. As the nuns expressed it,
			they depend very much on the villagers and the villagers need to bring them young nuns. Two nuns attended a Vinaya training program at Karsha
			Monastery.</p>

			<p>The nuns’ biggest hope for the future is that more young nuns will join the monastery and that they can develop a study program, but there are
			many obstacles. They felt that more girls will become nuns only if parents encourage their young daughters to learn to read Dharma texts from the
			nuns. Most parents these days send their children to school, but the medium of instruction is Urdu, so the children are forgetting their own language.
			In the past, the villagers often came to study texts at the monastery, so the nuns feel that if they had a resident teacher, more people would be
			interested in studying the Dharma. Perhaps if the nuns learned to present Dharma in a way that is more accessible to young people, more would come.
			The basic problem is that the nuns have not been to school, so it is difficult for them to connect with young people who have.</p>
