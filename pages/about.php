		<h4>New Possibilities</h4>
		<div class="medium-3 columns">
			<img src="../img/old/childnuns_sm.jpg"/>
		</div>
		<div class="medium-9 columns">
			<p>Beginning in 1985, courageous young Buddhist women have taken the initiative to establish study programs that combine traditional and contemporary learning.
			Traditional values are still strong in these areas, which have been home to Buddhist cultures for over a thousand years. Loving kindness and compassion guide
			women’s lives, but women have rarely emerged as spiritual leaders, due to a lack of educational opportunities. Social and economic changes now seriously threaten
			traditional values. At this juncture, it becomes more urgent than ever to give women equal social and educational opportunities. Overcoming many obstacles, women
			are preparing themselves to make valuable contributions in the fields of health, education, culture, social and economic development in their communities.</p>

			<p>Of the 387 students currently enrolled in Jamyang Foundation’s study programs, 255 are from remote Himalayan areas of India, ranging in age from 6 to 73. In
			addition, 132 young Marma girls live in remote areas of Bangladesh. Most have had little, if any, schooling. The reasons are significant: parents often send the
			boys to school and keep the girls at home to work. Furthermore, if there are any local schools, they teach an unfamiliar culture in an alien language. In the
			Himalayas, the students are from Tibetan Buddhist cultural backgrounds; the government schools teach Hindi and Urdu. In the Chittagong Hill Tracts, the students
			are Marma Buddhists; the government schools teach Bangla. Teachers in government schools often fail to show up for classes. Girls may face the danger of sexual
			assault on their way to school.</p>
