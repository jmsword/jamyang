		<h4>Courage Amidst Hardship</h4>
  		<div class="medium-4 medium-push-8 columns">
  			<a href="../img/IMG_0784.jpg"><img src="../img/IMG_0784.jpg"/></a>
  		</div>
  		<div class="medium-8 medium-pull-4 columns">
  			<p>Students in these remote regions live in conditions of poverty and isolation. In the Himalayas, the winters are incredibly harsh, with months of heavy snowfalls.
  			Temperatures often dip far below freezing, and food and water are often in short supply. In Bangladesh, the climate is the reverse, with unbearable heat much of the
  			year. Medical facilities are often non-existent. Women's lives are filled with perpetual hardships.</p>

  			<p>To address these problems, students in the Jamyang Foundation programs study languages, math, health and hygiene, social studies, environmental awareness, Buddhist
  			philosophy, and meditation. After completing their studies, these students will have the opportunity to go for higher studies and to serve as teachers, health care
  			workers, community workers, and mentors to others. The goal is to create greater opportunities for Buddhist women and more study programs in these needy areas. Women are
  			uniquely positioned to serve in community development, to link with other women around the world, and to help revitalize their own special cultural heritage.</p>
