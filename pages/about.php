        Several pioneering projects in India (Jamyang Chöling in Dharamsala and Sherab Chöling in Himachal Pradesh) and one school in Bangladesh (Sanghamitta) are now
        self-sufficient, fulfilling Jamyang Foundation’s mission of empowering women and creating sustainable communities run by women themselves.</p>
